    public void llenarTablaProductos(){
        ProductoLogica productoLogica = new ProductoLogica();
        List<Producto> listaConsultaUsuario = productoLogica.listarProductos();
        setTabla(listaConsultaUsuario); 
    }    

    public void setTabla(List<Producto> productos){
        modelo = (DefaultTableModel) productosTable.getModel();
        int size = modelo.getRowCount();
        for (int i = 0; i < size; i++) {
            modelo.removeRow(0);
        } 
        String aux[];
        for (int j = 0; j < productos.size(); j++) {
            aux = new String[7];
            aux[0] = productos.get(j).getProductoID().toString(); 
            aux[1] = productos.get(j).getNombreProducto();
            aux[2] = String.valueOf(productos.get(j).getPrecioProducto()); 
            aux[3] = productos.get(j).getDescripcionProducto();
            aux[4] = productos.get(j).getCategoriaProducto();
            aux[5] = productos.get(j).getSubcategoriaProducto();
            aux[6] = String.valueOf(productos.get(j).getCantidadDisponible());
            modelo.addRow(aux);
         }
    }    

